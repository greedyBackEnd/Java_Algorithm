package com.codeup.code1070;

import java.util.Scanner;

public class Codeup1075 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int iVal = sc.nextInt();
        for (int i = iVal-1; i >= 0; i--)
            System.out.println(i);
        sc.close();
    }
}
