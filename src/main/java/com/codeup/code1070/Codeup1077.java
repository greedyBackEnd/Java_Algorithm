package com.codeup.code1070;

import java.util.Scanner;

public class Codeup1077 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int iVal = sc.nextInt();
        for (int i = 0; i <= iVal; i++) {
            System.out.println(i);
        }
        sc.close();
    }
}
